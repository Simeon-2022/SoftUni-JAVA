package _14_Defining_Classes_Exercise._01_Opinion_Poll;

//_01_Opinion_Poll
public class Person {

    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
